def ascii_art(text: str) -> str:
    """Convert text to ASCII art."""
    text = text.lower()
    chars = [FONT.get(c, FONT[" "]) for c in text]
    normalized = []
    for char in chars:
        max_width = max(len(row) for row in char)
        normalized.append([row.ljust(max_width) for row in char])
    for i, char in enumerate(normalized):
        normalized[i] = [row + " " for row in char]
    lines = ["".join(row) for row in zip(*normalized)]
    return "\n".join(lines)


